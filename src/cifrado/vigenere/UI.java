    private void cifrarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cifrarBtnActionPerformed
        boolean error = false;
        if (datos.isCifrado()) {
            JOptionPane.showMessageDialog(null, "La palabra ya está cifrada");
        } else {
            datos.setCifrado(true);
            palabra = palabraTxt.getText();
            clave = claveTxt.getText().toLowerCase();
            for (int i = 0; i < clave.length(); i++) {
                if (clave.length() > palabra.length() || datos.abc.indexOf(clave.charAt(i)) < 0) {
                    JOptionPane.showMessageDialog(null, "Error con la clave,revise que no contenga caracteres especiales ni sea más larga que la frase a cifrar");
                    error = true;
                    break;
                }
            }
            if (error == false) {
                resultadoTtx.setText(datos.Cifrar(clave, palabra));
            }
        }

    }//GEN-LAST:event_cifrarBtnActionPerformed

