    private void descifrarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_descifrarBtnActionPerformed
        if (datos.isCifrado()) {
            palabra = resultadoTtx.getText();
            resultadoTtx.setText(datos.Descifrar(clave, palabra));
            datos.setCifrado(false);
        } else {
            JOptionPane.showMessageDialog(null, "La palabra ya está descifrada");
        }
    }//GEN-LAST:event_descifrarBtnActionPerformed

